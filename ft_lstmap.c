#include "includes/libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list *new;
	t_list *temp;
	t_list *head;

	if (!lst || !f)
		return (NULL);
	temp = f(lst);
	head = ft_lstnew(temp->content, temp->content_size);
	new = head;
	lst = lst->next;
	while (lst)
	{
		temp = f(lst);
		if (!(new->next = ft_lstnew(temp->content, temp->content_size)))
			return (NULL);
		new = new->next;
		lst = lst->next;
	}
	return (head);
}
