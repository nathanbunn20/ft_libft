#include "includes/libft.h"

char	*ft_rot42(char *str)
{
	int a;
	int b;

	a = 0;
	b = 0;
	if (!str)
		return (0);
	while (str[a] != '\0')
	{
		if (str[a] >= 'A' && str[a] <= 'Z')
		{
			b = str[a] - 22;
			b = b % 26;
			str[a] = b + 64;
		}
		if (str[a] >= 'a' && str[a] <= 'z')
		{
			b = str[a] - 54;
			b = b % 26;
			str[a] = b + 96;
		}
		a++;
	}
	return (str);
}
